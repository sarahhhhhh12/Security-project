            <div class="answer">
            <p>Miscommunication, cultural differences, diverging expectations or even just clashing personalities… these are all possible reasons that an exchange may not work out as planned.
                 If this happens, we recommend the best course of action is to try and calmly discuss the issues with each other. After all, workawayers travel far beyond their comfort zone to 
                 help and bond with hosts, and hosts are also opening their homes or projects to workawayers. So, it is always mutually beneficial for both parties if conflicts can be avoided or
                  resolved. If the issue still cannot be solved, remember safety always comes first and you are free to end any arrangement you are no longer comfortable with.</p>
